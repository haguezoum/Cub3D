#include "cub3d.h"

int distance(int x1, int y1, int x2, int y2)
{
    int x;
    int y;
    int z;
    
    x = x1 - x2;
    y = y1 - y2;
    z = sqrt((x * x) + (y * y));
    return (z);
}

